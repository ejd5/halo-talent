function randomIp() {
  const ranges = ["192.168.1.", "10.0.0.", "172.16.0.", "89.2.", "45.33."];
  const base = ranges[Math.floor(Math.random() * ranges.length)];
  return base + Math.floor(Math.random() * 255);
}

