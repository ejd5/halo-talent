// ─── Smart Label Computation ─────────────────────────────────

export function computeSmartLabels(asset: ContentAsset): SmartLabel[] {
  const labels: SmartLabel[] = [];

  if (asset.sentToFansCount > 0) {
    labels.push({ type: "already_sent", labelKey: "content_vault.label.already_sent" });
  }

  if (asset.soldCount > 0) {
    labels.push({ type: "already_sold", labelKey: "content_vault.label.already_sold" });
  }

  if (asset.fatigueScore > 60) {
    labels.push({ type: "fatigue_risk", labelKey: "content_vault.label.fatigue_risk" });
  }

  if (asset.performanceScore > 60 && asset.lastUsedAt) {
    const daysSince = Math.round((now - new Date(asset.lastUsedAt).getTime()) / 86400000);
    if (daysSince > 30) {
      labels.push({ type: "reactivation_candidate", labelKey: "content_vault.label.reactivate" });
    }
  }

  if (asset.premiumLevel === "high" && asset.performanceScore > 70) {
    labels.push({ type: "vip_candidate", labelKey: "content_vault.label.vip_candidate" });
  }

  if (asset.rightsStatus !== "validated") {
    labels.push({ type: "rights_issue", labelKey: "content_vault.label.rights_issue" });
  }

  if (asset.sensitivityLevel !== "standard") {
    labels.push({ type: "sensitive", labelKey: "content_vault.label.sensitive" });
  }

  return labels;
}

